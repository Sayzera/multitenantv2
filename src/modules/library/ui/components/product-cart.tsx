import { formatCurrency } from '@/lib/utils'
import { StarIcon } from 'lucide-react'
import Image from 'next/image'
import Link from 'next/link'

interface ProductCardProps {
  id: string
  name: string
  imageUrl?: string | null
  tenantSlug: string
  tenantImageUrl?: string | null
  reviewRating: number
  reviewCount: number
  price: number
}

export const ProductCard = ({
  id,
  name,
  imageUrl,
  tenantSlug,
  tenantImageUrl,
  reviewRating,
  reviewCount,
  price,
}: ProductCardProps) => {
  return (
    <Link href={`/library/${id}`}>
      <div
        className="
      hover:shadow-[4px_4px_0px_0px_rgba(0,0,0,1)] transition-shadow
      border rounded-md bg-white overflow-hidden h-full flex flex-col"
      >
        <div className="relative aspect-square">
          <Image alt={name} fill src={imageUrl || '/placeholder.png'} className="object-cover" />
        </div>

        <div className="p-4 border-y flex flex-col gap-3 flex-1">
          <h2 className="text-lg font-medium line-clamp-4">{name}</h2>
          {/* TODO: Redirect to user shop */}
          <div className="flex items-center gap-2">
            {tenantImageUrl && (
              <Image
                alt={tenantSlug}
                src={tenantImageUrl}
                width={16}
                height={16}
                className="rounded-full border shrink-0 size-[16px]"
              />
            )}
            <p className="text-sm underline font-medium">{tenantSlug}</p>
          </div>
          {reviewCount > 0 && (
            <div className="flex items-center gap-1">
              <StarIcon className="size-3.5 fill-black" />
              <p className="text-sm font-medium">
                {reviewRating} ({reviewCount})
              </p>
            </div>
          )}
        </div>
        <div className="p-4">
          <div className="relative px-2 py-1 border bg-pink-400 w-fit">
            <p className="text-sm font-medium">{formatCurrency(price)}</p>
          </div>
        </div>
      </div>
    </Link>
  )
}

export const ProductCardSkeleton = () => {
  return (
    <div
      className={
        ' transition-shadow border rounded-md bg-white overflow-hidden h-full flex flex-col animate-pulse'
      }
      aria-hidden
    >
      {/* Image placeholder */}
      <div className="relative aspect-square bg-neutral-200" />

      {/* Content */}
      <div className="p-4 border-y flex flex-col gap-3 flex-1">
        <div className="h-5 bg-neutral-200 rounded w-3/4" />

        <div className="flex items-center gap-2">
          <div className="w-4 h-4 rounded-full bg-neutral-200 shrink-0" />
          <div className="h-4 bg-neutral-200 rounded w-1/3" />
        </div>

        <div className="h-4 bg-neutral-200 rounded w-1/2" />
      </div>

      {/* Price placeholder */}
      <div className="p-4">
        <div className="relative px-2 py-1 border bg-neutral-200 w-fit">
          <span className="text-sm font-medium text-transparent">price</span>
        </div>
      </div>
    </div>
  )
}
